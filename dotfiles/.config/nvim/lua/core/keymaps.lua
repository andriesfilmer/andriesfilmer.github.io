-- Navigate vim panes better
vim.keymap.set('n', '<c-k>', ':wincmd k<CR>') -- , opts('Go pane right'))
vim.keymap.set('n', '<c-j>', ':wincmd j<CR>') -- , opts('Go pane down'))
vim.keymap.set('n', '<c-h>', ':wincmd h<CR>') -- , opts('Go pane left'))
vim.keymap.set('n', '<c-l>', ':wincmd l<CR>') -- , opts('Go pane up'))

vim.keymap.set('n', '<leader>h', ':nohlsearch<CR>')
vim.keymap.set('n', '<leader>h', ':nohlsearch<CR>')
